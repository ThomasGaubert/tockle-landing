			<h1 class="lobster">Privacy Policy</h1>
			<p>Revised January 12, 2016.</p>

			<h3>Overview</h3>
			<p>
				This privacy policy discloses the privacy practices for Tockle and
				associated services. This privacy policy applies solely to information collected 
				by this application and related products and services. This policy covers what 
				information is collected by Tockle and how that information is used. Questions 
				pertaining to the policy outlined below should be sent to tockleapp AT gmail DOT com.
			</p>
			
			<h3>What's Collected</h3>
			<p>
				Tockle relies on your device's various sensors to function properly. This can 
				include GPS, WiFi status, etc. Information from these sensors is only gathered 
				when the application actually needs the information. Additionally, general usage 
				stats are measured during the use of the application for analytical purposes. 
				For example, we track when ActionSets are activated and various metadata associated 
				with the ActionSet.
			</p>

			<h3>How it's Used</h3>
			<p>
				Information pertaining to the app's functionality, such as GPS location when using the 
				Location profile, is kept local to the device at all times. Unless the information needs 
				to be saved to a local file for later use (when it's a setting to a Profile, etc.), all 
				information is discarded after being used. At no point does Tockle upload personal 
				information which may identify you. Analytical data is completely anonymous and not linked 
				to a specific username, email address, or similar. Analytical data is used for development 
				purposes to ensure that appropriate focus is given on popular features (and similar purposes).
			</p>

			<h3>Who has Access</h3>
			<p>
				Personal information is only accessible to you through the use of Tockle as this information 
				is kept local to the device. Note that voluntary bug reports may contain personal information 
				(but they usually do not) which can be seen by Tockle developers. Analytical data is uploaded 
				to Google Analytics and is only accessible to Tockle developers and Google as outlined in the 
				Google Analytics Privacy Policy and Terms of Use.
			</p>

			<h3>Revisions</h3>
			<p>
				While we will always strive to be transparent when discussing privacy and any changes if needed 
				in regards to it, we reserve the right to update this privacy policy without notice. All changes 
				will be posted to this site and the revision date will be changed accordingly.
			</p>
